    def generateSequence(self):
        self.stepCounter = 0
        self.flag=1
        memoryManagement.blockPage=[[-1 for i in range(4)] for i in range(2)]
        memoryManagement.blockFIFOQueue=[-1]*320
        memoryManagement.queueHead=0
        memoryManagement.queueCurrent=0
        memoryManagement.blockLRUSignal=[0]*4
        memoryManagement.pageNumber=[0]*2
