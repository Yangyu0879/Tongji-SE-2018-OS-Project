    def singleStep(self):
        if self.flag==1 and self.stepCounter<=319:
            newPage = self.pages[self.stepCounter]
            memoryManagement.algorithmFIFO(newPage)
            memoryManagement.algorithmLRU(newPage)
            self.listWidget_9.setCurrentRow(self.stepCounter)
            self.listWidget_10.setCurrentRow(self.stepCounter)
            self.stepCounter += 1
            self.refreshDisplay()

    def ToEnd(self):
        while self.stepCounter <= 319:
            lock.acquire()
            self.singleStep()
            lock.release()
            time.sleep(0.1) 
            
    def goToEnd(self):
        if self.flag==1:
            t0 = Thread(target=self.ToEnd,args=())
            t0.start()

